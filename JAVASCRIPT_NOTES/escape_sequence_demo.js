// To print a new line in the same string  we use '\n'
console.log("Hello how are you ?\nI am good what about you ? ");

// To provide a tab space we use '\t'
console.log("Hello \t how are you ?");

// to print backslash  we use '\\'
console.log("I am going to print backslash \\ by using double backslash ");

// To print double quote within a string we use \"...\"
console.log('I will print "double quotes" within a string.');

// To print single quotes within a string  we use \'...\'
console.log("I will print 'single quotes' within a string.");

